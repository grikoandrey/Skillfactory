def send_email_to_zoo(results, subject):  # —Ñ—É–Ω–∫—Ü–∏—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –∏—Ç–æ–≥–æ–≤ –Ω–∞ –ø–æ—á—Ç—É —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–∞
    sender = config('GMAIL_USER')
    password = config('GMAIL_KEY_PYTHON')
    recipient = 'MAILRU_USER'

    msg = MIMEText(results)  # —Å–æ–∑–¥–∞–µ–º –æ–±—ä–µ–∫—Ç —Å–æ–æ–±—â–µ–Ω–∏—è
    msg['Subject'] = subject  # '–†–µ–∑—É–ª—å—Ç–∞—Ç—ã –≤–∏–∫—Ç–æ—Ä–∏–Ω—ã –æ—Ç –ó–æ–æ-–±–æ—Ç–∞'
    try:
        with smtplib.SMTP('smtp.gmail.com', 587) as server:  # –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ —Å–µ—Ä–≤–µ—Ä—É SMTP
            server.starttls()
            server.login(sender, password)
            server.sendmail(sender, recipient, msg.as_string())
            print('Success!')
            return True
    except Exception as e:
        print(f'{e}\n Check login –∏–ª–∏ password, please.')
        return False


