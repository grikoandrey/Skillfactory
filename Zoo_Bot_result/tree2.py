class RandomTextGenerator:  # –∫–ª–∞—Å—Å –¥–ª—è —Ä–∞–Ω–¥–æ–º–Ω–æ–π –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–π –∏–∑ —Ñ–∞–π–ª–æ–≤
    def __init__(self, file_path):
        self.file_path = file_path
        self.text_list = self.read_text_from_file()

    def read_text_from_file(self):
        if not os.path.isfile(self.file_path):
            print(f"–í–Ω–∏–º–∞–Ω–∏–µ: –§–∞–π–ª '{self.file_path}' –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç.")
            return []
        with open(self.file_path, 'r', encoding='utf-8') as file:
            text_list = file.readlines()
        return text_list

    def get_random_text(self):
        if not self.text_list:
            return f"–§–∞–π–ª '{self.file_path}' –ø—É—Å—Ç."

        random_text = random.choice(self.text_list)
        return random_text.strip()


