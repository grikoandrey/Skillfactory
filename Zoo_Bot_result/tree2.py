def choose_animal(user_params, choices):  # —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ –±–ª–∏–∑–∫–æ–≥–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è
    best_match = None
    min_difference = float('inf')  # –ù–∞—á–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–π —Ä–∞–∑–Ω–∏—Ü—ã (–ø—Ä–∏–Ω–∏–º–∞–µ–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ)

    for params, animal in choices.items():  # –ü—Ä–æ—Ö–æ–¥–∏–º—Å—è –ø–æ –∫–∞–∂–¥–æ–º—É –≤–∞—Ä–∏–∞–Ω—Ç—É –≤ —Å–ø–∏—Å–∫–µ –∂–∏–≤–æ—Ç–Ω—ã—Ö
        difference = sum(abs(user_param - param) for user_param, param in zip(user_params, params))
        # –°—á–∏—Ç–∞–µ–º —Å—É–º–º—É —Ä–∞–∑–Ω–∏—Ü –º–µ–∂–¥—É –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏ —Ç–µ–∫—É—â–µ–≥–æ –∂–∏–≤–æ—Ç–Ω–æ–≥–æ

        if difference < min_difference:  # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∞—è —Ä–∞–∑–Ω–∏—Ü–∞ –º–µ–Ω—å—à–µ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–π
            min_difference = difference  # –û–±–Ω–æ–≤–ª—è–µ–º –º–∏–Ω–∏–º–∞–ª—å–Ω—É—é —Ä–∞–∑–Ω–∏—Ü—É
            best_match = animal  # –û–±–Ω–æ–≤–ª—è–µ–º –ª—É—á—à–µ–µ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ

    return best_match  # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –ª—É—á—à–µ–µ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ


