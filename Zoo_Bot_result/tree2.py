def load_quiz_data(quiz_data):  # –∑–∞–≥—Ä—É–∑–∫–∞ –¥–∞–Ω–Ω—ã—Ö –¥–ª—è –≤–∏–∫—Ç–æ—Ä–∏–Ω—ã (–≤–æ–ø—Ä–æ—Å–æ–≤ –∏ –æ—Ç–≤–µ—Ç–æ–≤)
    file_path = os.path.join("for_Bot/zoo_info", f"{quiz_data.lower()}")
    if not os.path.isfile(file_path):
        return f"–§–∞–π–ª {quiz_data} –Ω–µ –æ–±–Ω–∞—Ä—É–∂–µ–Ω."
    questions = []
    with open(file_path, 'r', encoding='utf-8') as file:
        question_data = {}
        is_question = False
        for line in file:
            line = line.strip()
            if line.startswith("question:"):  # –ù–∞—á–∞–ª–æ –Ω–æ–≤–æ–≥–æ –≤–æ–ø—Ä–æ—Å–∞
                is_question = True
                if question_data:  # –î–æ–±–∞–≤–ª—è–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π –≤–æ–ø—Ä–æ—Å
                    questions.append(question_data)
                question_data = {"question": line.split(": ", 1)[1]}  # –ü–æ–ª—É—á–∞–µ–º —Ç–µ–∫—Å—Ç –≤–æ–ø—Ä–æ—Å–∞
            elif line.startswith("answers:"):  # –û—Ç–≤–µ—Ç—ã –∫ —Ç–µ–∫—É—â–µ–º—É –≤–æ–ø—Ä–æ—Å—É
                is_question = False
                answers = []
                for ans in line.split(": ")[1].split("; "):
                    answer_number, ans_text_rank = ans.split(". ")  # –†–∞–∑–¥–µ–ª—è–µ–º –Ω–æ–º–µ—Ä –æ—Ç–≤–µ—Ç–∞ –∏ —Ç–µ–∫—Å—Ç —Å —Ä–∞–Ω–≥–æ–º
                    ans_text, rank = ans_text_rank.split(":")  # –†–∞–∑–¥–µ–ª—è–µ–º —Ç–µ–∫—Å—Ç –æ—Ç–≤–µ—Ç–∞ –∏ –µ–≥–æ —Ä–∞–Ω–≥
                    # text, rank = ans.split(":")
                    answers.append({"number": int(answer_number),
                                    "text": ans_text.strip(), "rank": float(rank.strip())})  # –ò–∑–º–µ–Ω—è–µ–º —Å—Ç—Ä—É–∫—Ç—É—Ä—É
                question_data["answers"] = answers
            elif is_question:  # –ï—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ –ø—Ä–∏–Ω–∞–¥–ª–µ–∂–∏—Ç –≤–æ–ø—Ä–æ—Å—É
                question_data["question"] += "\n" + line
        if question_data:  # –î–æ–±–∞–≤–ª—è–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–π –≤–æ–ø—Ä–æ—Å
            questions.append(question_data)
    return questions


